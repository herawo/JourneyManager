    public void refreshList(){
        displayer = JourneyManager.getInstance();
        DefaultTableModel model = new DefaultTableModel(
                displayer.getJourneyArray(),
                new Object[] {"Pays","Ville","Prix","Places"}
        );
        journey_table.setModel(model);
    }
    

