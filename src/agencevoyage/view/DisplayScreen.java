    private void modifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifierActionPerformed
        if(journey_table.getSelectedRow() != -1){
            currentframe.getContentPane().removeAll();
            Journey user_item = displayer.getJourneyList().get(journey_table.getSelectedRow());
            ModifyJourneyScreen modify = new ModifyJourneyScreen(contentPane, currentframe, user_item);
            currentframe.getContentPane().add(modify);
            currentframe.getContentPane().validate();
        }
    }//GEN-LAST:event_modifierActionPerformed

