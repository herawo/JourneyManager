    private void refreshComboBox(){
        String[] places = journey.getPlacesArray();
        DefaultComboBoxModel cbm = new DefaultComboBoxModel(places);
        nb_place_box.setModel(cbm);
    }
    
    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        int booked_place = Integer.parseInt((String) nb_place_box.getSelectedItem());
        int new_number_place = journey.getNumberPlace() - booked_place;
        if(new_number_place <= 0){
            journey.setNumberPlace(new_number_place);
            refreshComboBox();
            currentframe.getContentPane().removeAll();
            DisplayScreen displayer_screen = new DisplayScreen(
                    contentPane, 
                    currentframe
            );
            currentframe.getContentPane().add(displayer_screen);
            currentframe.getContentPane().validate();
        }
        else{
            return;
        }
    }//GEN-LAST:event_submitActionPerformed


